/* =================================================================
   app.js (GitHub Pages edition)
   Reads from docs/data/signals.json, generated by GitHub Actions.
   Auto-refreshes every 60 minutes in case Actions has run since load.
   ================================================================= */

const SIGNALS_URL      = './data/signals.json';
const REFRESH_INTERVAL = 60 * 60 * 1000;  // 60 minutes
const STALE_THRESHOLD  = 8 * 60 * 60 * 1000; // warn if data older than 8 hours

let state        = null;
let refreshTimer = null;
let chartsInited = false;

// ── Data fetching ─────────────────────────────────────────────────

async function fetchSignals() {
  // Cache-bust so the browser always gets the latest file from GitHub Pages CDN
  const url = `${SIGNALS_URL}?t=${Date.now()}`;
  const res = await fetch(url);
  if (!res.ok) throw new Error(`HTTP ${res.status} — ${res.statusText}`);
  return res.json();
}

// ── UI helpers ────────────────────────────────────────────────────

function showBanner(type, msg) {
  const el = document.getElementById('banner');
  el.className = `banner ${type}`;
  el.textContent = msg;
  el.style.display = 'block';
}

function hideBanner() {
  document.getElementById('banner').style.display = 'none';
}

function updateTimestamp(iso) {
  const el  = document.getElementById('last-updated');
  if (!iso) { el.textContent = '—'; return; }

  const ts      = new Date(iso);
  const ageMs   = Date.now() - ts.getTime();
  const ageHrs  = (ageMs / 3_600_000).toFixed(1);
  const ageText = ageMs < 3_600_000
    ? `${Math.floor(ageMs / 60_000)}m ago`
    : `${ageHrs}h ago`;

  el.textContent = `Signals generated: ${ts.toLocaleString()} (${ageText})`;

  // Stale warning: older than threshold on a weekday
  const day = ts.getDay();
  const isWeekday = day >= 1 && day <= 5;
  if (isWeekday && ageMs > STALE_THRESHOLD) {
    showBanner('warning', `⚠ Signal data is ${ageHrs} hours old. GitHub Actions may not have run yet today.`);
  } else {
    hideBanner();
  }
}

function setSpinner(on) {
  document.getElementById('refresh-spinner').style.display = on ? 'inline' : 'none';
}

// ── Detail chart panel ────────────────────────────────────────────

function handleExpand(etf) {
  const section    = document.getElementById('detail-section');
  const tickerSpan = document.getElementById('detail-ticker');

  if (!etf) {
    section.style.display = 'none';
    destroyDetailCharts();
    return;
  }

  tickerSpan.textContent = etf.ticker;
  section.style.display  = 'block';
  section.scrollIntoView({ behavior: 'smooth', block: 'start' });
  showDetailCharts(etf);
}

document.getElementById('close-detail').addEventListener('click', () => {
  if (state) renderCards(state.etfs, state.sentiment, handleExpand);
  handleExpand(null);
});

// ── Main refresh ──────────────────────────────────────────────────

async function refresh() {
  setSpinner(true);
  try {
    const data = await fetchSignals();
    state = data;

    updateRegimeBadge(data.marketRegime);
    updateSentimentPanel(data.sentiment);
    renderCards(data.etfs, data.sentiment, handleExpand);

    if (!chartsInited) {
      initPerfChart(data.etfs);
      chartsInited = true;
    } else {
      updatePerfChart(data.etfs);
    }

    updateTimestamp(data.timestamp);
  } catch (err) {
    console.error('[refresh]', err);
    // No signals.json yet — likely first run before Actions has executed
    showBanner('warning',
      'Signal data not available yet. GitHub Actions will generate it shortly — try again in a few minutes.');
  } finally {
    setSpinner(false);
  }

  clearTimeout(refreshTimer);
  refreshTimer = setTimeout(refresh, REFRESH_INTERVAL);
}

// Refresh when tab becomes visible again
document.addEventListener('visibilitychange', () => {
  if (!document.hidden) refresh();
});

document.getElementById('refresh-btn').addEventListener('click', () => {
  clearTimeout(refreshTimer);
  refresh();
});

refresh();
